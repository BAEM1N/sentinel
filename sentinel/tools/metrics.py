def _default_range(days_back: int = 7):
    """기본 날짜 범위를 반환합니다."""
    now = datetime.utcnow()
    return (
        (now - timedelta(days=days_back)).strftime("%Y-%m-%dT%H:%M:%SZ"),
        now.strftime("%Y-%m-%dT%H:%M:%SZ"),
    )


@tool
def query_metrics(
    view: str = "traces",
    metrics: str = "count,totalCost,latency",
    group_by: str = "name",
    period: str = "day",
    from_ts: str = "",
    to_ts: str = "",
    filter_name: str = "",
    filter_user_id: str = "",
    filter_environment: str = "",
    filter_release: str = "",
    filter_model: str = "",
) -> str:
    """Langfuse Metrics API로 집계 분석을 수행합니다.

    Args:
        view: 조회 대상 (traces, observations, scores-numeric)
        metrics: 집계 지표 (쉼표 구분: count, totalCost, latency, totalTokens)
        group_by: 그룹핑 차원 (name, environment, model 등)
        period: 시간 단위 (hour, day, week, month)
        from_ts: 시작 날짜 (ISO8601, 비우면 최근 7일)
        to_ts: 종료 날짜 (ISO8601, 비우면 현재)
        filter_name: 트레이스 이름 필터
        filter_user_id: 사용자 ID 필터
        filter_environment: 환경 필터 (e.g. production, staging)
        filter_release: 릴리스 필터
        filter_model: 모델 필터
    """
    if not from_ts or not to_ts:
        default_from, default_to = _default_range(7)
        from_ts = from_ts or default_from
        to_ts = to_ts or default_to

    agg_map = {
        "count": "count",
        "totalCost": "sum",
        "latency": "p50",
        "totalTokens": "sum",
    }
    metric_list = [
        {"measure": m.strip(), "aggregation": agg_map.get(m.strip(), "sum")}
        for m in metrics.split(",")
    ]

    q: dict = {
        "view": view,
        "dimensions": [{"field": group_by}],
        "metrics": metric_list,
        "timeDimension": {"granularity": period},
        "fromTimestamp": from_ts,
        "toTimestamp": to_ts,
        "filters": [],
        "orderBy": [],
        "rowLimit": 100,
    }
    if filter_name:
        q["filters"].append(
            {"column": "name", "operator": "equals", "value": filter_name, "type": "string"}
        )
    if filter_user_id:
        q["filters"].append(
            {"column": "userId", "operator": "equals", "value": filter_user_id, "type": "string"}
        )
    if filter_environment:
        q["filters"].append(
            {"column": "environment", "operator": "equals", "value": filter_environment, "type": "string"}
        )
    if filter_release:
        q["filters"].append(
            {"column": "release", "operator": "equals", "value": filter_release, "type": "string"}
        )
    if filter_model:
        q["filters"].append(
            {"column": "model", "operator": "equals", "value": filter_model, "type": "string"}
        )

    try:
        result = lf_client.api.metrics.metrics(query=json.dumps(q))
    except Exception as e:
        logger.exception("Metrics API 호출 실패")
        return json.dumps({"error": f"Langfuse Metrics API 오류: {e}"}, ensure_ascii=False)
    return json.dumps(result, ensure_ascii=False, indent=2, default=str)


